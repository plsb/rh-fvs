    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if (Util.chkVazio(tfLogin.getText(),tfSenha.getText(),tfConfirmaSenha.getText())) {
            if (tfSenha.getText().equals(tfConfirmaSenha.getText())) {
                
                u.setLogin(tfLogin.getText());
                u.setSenha(tfSenha.getText());
                u.setAdministrador(chbAdministrado.isSelected());

                UsuarioDAO dao = new UsuarioDAO();
                dao.salvar(u);
                limpaCampos();

                JOptionPane.showMessageDialog(rootPane, "Cadastro Efetuado");
            } else {
                JOptionPane.showMessageDialog(rootPane, "Confirma√ß√£o de Senha n√£o confere com a Senha");
            }

        } 
    }//GEN-LAST:event_jButton1ActionPerformed

    private void btRemoverActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btRemoverActionPerformed
        UsuarioDAO dao = new UsuarioDAO();
        dao.remover(u);
        limpaCampos();

        JOptionPane.showMessageDialog(rootPane, "Usu√°rio Exclu√≠do");
    }//GEN-LAST:event_btRemoverActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        UsuarioDAO dao = new UsuarioDAO();
        List<Usuario> usuarios = (!tfLogin.getText().isEmpty() ? dao.pesquisaDescricao(tfLogin.getText())
                : dao.listar());

        UsuarioTableModel utm = new UsuarioTableModel(usuarios);

        Object o = TelaPesquisa.exibeTela(utm, "Usuario");

        if (o != null) {        
            u = dao.pesquisaId(Integer.valueOf(String.valueOf(o)));

            tfLogin.setText(u.getLogin());
            chbAdministrado.setSelected(u.isAdministrador());

            btRemover.setEnabled(true);
        }
    }//GEN-LAST:event_jButton3ActionPerformed

