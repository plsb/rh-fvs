    private void limpaCampos() { //limpar campos
        tfDescricao.setText("");
        tfTurno.setText("");
        tfqdtHoras.setText("");
        horario = new Horario();
        btRemover.setEnabled(false);
        tfHoraInicio.setText("");
        tfHoraFim.setText("");
    }
